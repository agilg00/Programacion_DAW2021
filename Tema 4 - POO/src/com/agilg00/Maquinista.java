package com.agilg00;

public class Maquinista {

    public String nombre_completo;
    public String DNI;
    public int sueldo_mensual;
    public String rango;

    public Maquinista(String nombre_completo, String DNI, int sueldo_mensual, String rango) {
        this.nombre_completo = nombre_completo;
        this.DNI = DNI;
        this.sueldo_mensual = sueldo_mensual;
        this.rango = rango;
    }
}
